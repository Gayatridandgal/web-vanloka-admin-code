export const statusVariant = (s: Trainee['status']): 'green' | 'slate' | 'blue' | 'purple' => {
    switch (s) {
        case 'Active':
            return 'green';
        case 'Inactive':
            return 'slate';
        case 'In Training':
            return 'blue';
        case 'Certified':
            return 'purple';
    }
};

